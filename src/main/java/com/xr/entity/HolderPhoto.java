package com.xr.entity;


/**
 * 员工照片表(HolderPhoto)
 * @author csc
 * 实体类
 */
public class HolderPhoto {
    private Integer id;

    private String holderno;
    
    private byte[] dataphoto;

    private byte[] idphoto;


    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getHolderno() {
        return holderno;
    }

    public void setHolderno(String holderno) {
        this.holderno = holderno == null ? null : holderno.trim();
    }
    
    public byte[] getDataphoto() {
        return dataphoto;
    }

    public void setDataphoto(byte[] dataphoto) {
        this.dataphoto = dataphoto;
    }

    public byte[] getIdphoto() {
        return idphoto;
    }

    public void setIdphoto(byte[] idphoto) {
        this.idphoto = idphoto;
    }
}