package com.xr.entity;

/**
 * 考勤人员经常用到的班次表
 * @author CSC
 */
public class KQ_OftenShiftData {

	 private Integer oftenno;//该表主键
	 private String holderno;//员工工号
	 private Integer shiftno;//班次编号
	 
	 
	 //无参构造
	 public KQ_OftenShiftData() {
		super();
	 }

	//有参构造
	public KQ_OftenShiftData(Integer oftenno, String holderno, Integer shiftno) {
		super();
		this.oftenno = oftenno;
		this.holderno = holderno;
		this.shiftno = shiftno;
	}
	
	//get和set方法
	public Integer getOftenno() {
		return oftenno;
	}
	public void setOftenno(Integer oftenno) {
		this.oftenno = oftenno;
	}
	public String getHolderno() {
		return holderno;
	}
	public void setHolderno(String holderno) {
		this.holderno = holderno;
	}
	public Integer getShiftno() {
		return shiftno;
	}
	public void setShiftno(Integer shiftno) {
		this.shiftno = shiftno;
	}
	 
	 
}
