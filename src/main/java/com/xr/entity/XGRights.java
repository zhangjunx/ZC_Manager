package com.xr.entity;

/**
 *  权限表(XGRights)
 * @author csc
 *  实体类
 */
public class XGRights {

	 private Integer  rightsid;//RightsID主键
	 
	 private String  holderno;//用户工号
	 
	 private Integer routeid;//RouteID巡更线路主键
	 
     
	 //无参构造
	public XGRights() {
		super();
	}
	
	
     public XGRights(String holderno, Integer routeid) {
		super();
		this.holderno = holderno;
		this.routeid = routeid;
	}



	//GET和SET方法
	public Integer getRightsid() {
		return rightsid;
	}

	public void setRightsid(Integer rightsid) {
		this.rightsid = rightsid;
	}

	public String getHolderno() {
		return holderno;
	}
	
	public void setHolderno(String holderno) {
		this.holderno = holderno;
	}
	
	public Integer getRouteid() {
		return routeid;
	}

	public void setRouteid(Integer routeid) {
		this.routeid = routeid;
	}

}
